create DATABASE IF NOT EXISTS RETAILSTORE;
USE RETAILSTORE;
CREATE TABLE CUSTOMERS(
    CUSTOMERID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(100)
);
CREATE TABLE ORDERS(
    ORDERID INT AUTO_INCREMENT PRIMARY KEY,
    PRODUCT VARCHAR(100),
    AMOUNT INT ,
    CUSTOMERID INT,
    FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID)
);

